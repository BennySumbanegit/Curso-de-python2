nome='BENE-SUNBANE'


print(nome[3:8])
print(nome[3:8:2])
print(nome[:3])
print(nome[3:])
print(nome[2::2])


""" o (nome[3 ou 4 ou 5 ,etc ])- vai pegar esse tal caracter """
""" o (nome[3:8])- vai pegar do terceiro ate o setimo """
""" o (nome[3:8:2])- vai pegar do terceiro ate o setimo pulando de dois em dois  """
""" o (mome[:3])- vai comecar do inicio ate 2 """
""" o (nome[3:])- )vai comecar do tres ate o final """
""" o (nome[2::2])- vai comecar do dois ate o final comtando de dois  em dois """
""" geralmente o primeiro numero sera o inicio e ultimo sera o fim """
""" o primeiro numero sempre sera 0 """
""" cada letra ou espaco corresponde a um caracter ou espaco de um numero """