nome='bene-SUMBANE'

print(nome.replace('SUMBANE','ARNALDO'))
print(nome.upper())
print(nome.lower())
print(nome.capitalize())
print(nome.title())

""" o  nome.replace('SUMBANE','ARNALDO')- usamos para substituir uma palavra noutra """
""" o nome.upper()- usamos para colocar tudo em maiusculas """
""" o nome.lower()- usamos para colocar tudo em menusculas """
""" o nome.capitalize()- usamos para colocar a primeira letra em maiuscula e o resto em minusculas """
""" o nome.title()- usamos para deixar todas as primeiras letras inicias em maiusculas """